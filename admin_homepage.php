// 检查是否为AJAX请求
if (isset($_POST['type'])) {
    $type = $_POST['type'];
    $monthYear = isset($_POST['month']) ? $_POST['month'] : '';
    $section = isset($_POST['section']) ? $_POST['section'] : 'gs';

    // 从月份字段中提取年份和月份
    $year = '';
    $month = '';
    if ($monthYear) {
        list($year, $month) = explode('-', $monthYear);
    }

    // 根据section选择表格
    $table = ($section === 'nct') ? 'nct_sales' : 'gs_sales';

    // 设置查询条件
    $categoryCondition = "category = 'Order'";
    if ($type === 'salesPlusFreight') {
        $categoryCondition = "category IN ('Order', 'Freight')";
    }

    // 设置日期条件
    $dateCondition = '';
    if ($month && $year) {
        $dateCondition = "AND MONTH(shipping_date) = ? AND YEAR(shipping_date) = ?";
    }

    // 获取当前用户ID
    $userID = $_SESSION['UserID'];

    // 预处理SQL语句
    $sql = "SELECT SUM(amount) AS total FROM $table WHERE UserID = ? AND $categoryCondition $dateCondition";
    $stmt = $conn->prepare($sql);

    // 如果有月份和年份，绑定参数
    if ($month && $year) {
        $stmt->bind_param('iii', $userID, $month, $year);
    } else {
        $stmt->bind_param('i', $userID);
    }

    // 执行查询
    $stmt->execute();
    $result = $stmt->get_result();

    if ($result) {
        $row = $result->fetch_assoc();
        $amount = $row['total'] ? $row['total'] : 0.00;
        $response = array('status' => 'success', 'amount' => $amount);
    } else {
        $response = array('status' => 'error', 'message' => '计算金额失败。');
    }

    // 返回JSON响应
    header('Content-Type: application/json');
    echo json_encode($response);
    exit();
}

