    $(document).ready(function() {
        // Function to update amount for GS and NCT
        function updateAmount(section) {
            var filterType = $('input[name="salesFilter"]:checked').val();
            var monthYear = $('#month-filter').val(); // 获取年月字段

            $.ajax({
                url: 'homepage.php',
                type: 'POST',
                data: {
                    type: filterType,
                    section: section,
                    month: monthYear // 发送完整的年月
                },
                dataType: 'json',
                success: function(response) {
                    if (response.status === 'success') {
                        if (section === 'gs') {
                            $('#gs-amount').text('RM ' + parseFloat(response.amount).toFixed(2));
                        } else if (section === 'nct') {
                            $('#nct-amount').text('RM ' + parseFloat(response.amount).toFixed(2));
                        }
                        // Update Grand Total
                        var gsAmount = parseFloat($('#gs-amount').text().replace('RM ', '')) || 0;
                        var nctAmount = parseFloat($('#nct-amount').text().replace('RM ', '')) || 0;
                        var grandTotal = gsAmount + nctAmount;
                        $('#grand-total').text('RM ' + grandTotal.toFixed(2));
                    } else {
                        alert(response.message);
                    }
                }
            });
        }

        // Update amount when Apply button is clicked
        $('#apply-filter').on('click', function() {
            updateAmount('gs');
            updateAmount('nct');
        });

        // Clear filter when Clear button is clicked
        $('#clear-filter').on('click', function() {
            $('#month-filter').val(''); // 清空月份输入框
            $('#gs-amount').text('RM 0.00'); // 重置金额显示
            $('#nct-amount').text('RM 0.00'); // 重置金额显示
            $('#grand-total').text('RM 0.00'); // 重置总金额显示
        });

        // Optional: Update amount when radio buttons change
        $('input[name="salesFilter"]').on('change', function() {
            updateAmount('gs');
            updateAmount('nct');
        });

        // Optional: Set default values on page load
        updateAmount('gs');
        updateAmount('nct');
    });
    </script>


