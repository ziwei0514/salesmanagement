    function fetchChartData(year, month) {
        $.ajax({
            url: 'getChartData.php',
            type: 'POST',
            data: { year: year, month: month },
            dataType: 'json',
            success: function(response) {
                if (response.status === 'success') {
                    renderChart('chartCanvas', response.labels, response.gsData, response.nctData);
                } else {
                    alert(response.message);
                }
            }
        });
    }

    function renderChart(canvasId, labels, gsData, nctData) {
        var ctx = document.getElementById(canvasId).getContext('2d');

        new Chart(ctx, {
            type: 'bar',
            data: {
                labels: labels,
                datasets: [
                    {
                        label: 'GS Sales',
                        backgroundColor: 'rgba(75, 192, 192, 0.5)',
                        data: gsData
                    },
                    {
                        label: 'NCT Sales',
                        backgroundColor: 'rgba(255, 99, 132, 0.5)',
                        data: nctData
                    }
                ]
            },
            options: {
                responsive: true,
                scales: {
                    x: {
                        stacked: true
                    },
                    y: {
                        stacked: true
                    }
                }
            }
        });
    }
