    // JavaScript to handle form visibility
function showForm(formId) {
    // Hide all forms
    document.querySelectorAll('.collapse').forEach(function (element) {
        if (element.id !== formId) {
            var bsCollapse = new bootstrap.Collapse(element, { toggle: false });
            bsCollapse.hide();
        }
    });

    // Show the selected form
    var targetForm = document.getElementById(formId);
    var bsCollapse = new bootstrap.Collapse(targetForm, { toggle: true });
}

</script>

