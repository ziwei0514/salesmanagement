<script>
        function checkInvoiceExists(input, table, errorElement) {
            const invoice_no = input.value;
            if (invoice_no.trim() === "") {00000000
                errorElement.textContent = "";
                return;
            }

            const xhr = new XMLHttpRequest();
            xhr.open('GET', `check_invoice.php?invoice_no=${encodeURIComponent(invoice_no)}&table=${encodeURIComponent(table)}`, true);
            xhr.onload = function() {
                if (xhr.status === 200) {
                    const response = JSON.parse(xhr.responseText);
                    if (response.exists) {
                        errorElement.textContent = "Invoice NO already exists.";
                    } else {
                        errorElement.textContent = "";
                    }
                }
            };
            xhr.send();
        }
